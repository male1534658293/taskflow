// ─── 合并导出 ─────────────────────────────────────────────────────────────────
const ALL_HOLIDAYS = { ...holidays2025, ...holidays2026 }
const ALL_WORKDAYS = new Set([...workdays2025, ...workdays2026])

/**
 * 获取某日的节日名称，没有则返回 null
 */
export function getHolidayName(dateStr) {
  return ALL_HOLIDAYS[dateStr] || null
}

/**
 * 该日是否为调休补班（周末须上班）
 */
export function isWorkday(dateStr) {
  return ALL_WORKDAYS.has(dateStr)
}

/**
 * 获取某日的日历类型：
 *   'holiday'  — 法定假日
 *   'workday'  — 调休补班（原周末）
 *   'weekend'  — 普通周末（周六/周日）
 *   'normal'   — 普通工作日
 */
export function getDayType(dateStr) {
  if (ALL_HOLIDAYS[dateStr]) return 'holiday'
  if (ALL_WORKDAYS.has(dateStr)) return 'workday'
  const d = new Date(dateStr + 'T00:00:00')
  const dow = d.getDay()
  if (dow === 0 || dow === 6) return 'weekend'
  return 'normal'
}
